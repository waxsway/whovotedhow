// State governors and DC mayor. The state-level executive who signs
// or vetoes legislation passed by the state house. Hand-curated
// because a programmatic data source doesn't exist that's both
// authoritative and well-shaped (the National Governors Association
// has member pages but no clean API; Wikipedia is unreliable for
// recent transitions).
//
// Each entry's facts are independently verifiable from the governor's
// official state .gov page (linked in officialUrl). This file should
// be updated after every gubernatorial election (Nov of even years
// for most states; NJ and VA in odd years).
//
// Last full audit: 2026-06-21.

